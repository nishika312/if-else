num1=int(input("enter the number"))
num2=int(input("enter the number"))
operation=input("enter the operator")

if operation=="+":
    print(num1+num2)
elif operation=="-":
    print(num1-num2)
elif operation=="*":
    print(num1*num2)
elif operation=="**":
    print(num1*num2)
elif operation=="/":
    print(num1/num2)
elif operation=="%":
    print(num1%num2)
else:
    print(num1//num2)
    

